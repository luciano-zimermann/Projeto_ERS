    public void getTabelaEmbalagem() {
        Conexao_Embalagem conexao_Embalagem = new Conexao_Embalagem();
        Vector cabecalho = new Vector();
        Vector linhas = new Vector();
        
        cabecalho.add("Descrição");
        cabecalho.add("Comprimento");
        cabecalho.add("Largura");
        cabecalho.add("Altura");
        
        for (int i = 0; i < conexao_Embalagem.listar().size(); i++) 
        {
            Vector colunas = new Vector();
            colunas.add((conexao_Embalagem.listar().get(i).getDescricao()));
            colunas.add(conexao_Embalagem.listar().get(i).getComprimento());
            colunas.add((conexao_Embalagem.listar().get(i).getLargura()));
            colunas.add((conexao_Embalagem.listar().get(i).getAltura()));

            linhas.add(new Vector(colunas));
        }

        tblListaEmbalagens.setModel(new DefaultTableModel(linhas, cabecalho));
    }

