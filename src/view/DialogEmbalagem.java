    private void btnExcluirEmbalagemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirEmbalagemActionPerformed
        Conexao_Embalagem conexao_Embalagem = new Conexao_Embalagem();
        
        int embalagemSelecionada = tblListaEmbalagens.getSelectedRow();
        
        if (embalagemSelecionada >=0) 
        {
            Conexao_Embalagem ce = new Conexao_Embalagem();
            Embalagem embalagem = ce.listar().get(embalagemSelecionada);
            if (JOptionPane.showConfirmDialog( this, "Deseja realmente excluir essa embalagem?") == JOptionPane.YES_OPTION) 
            {
              ce.excluir(embalagem);
              getTabelaEmbalagem();
              JOptionPane.showMessageDialog(null, "Embalagem excluída com sucesso!");
            } 
            
            else 
            {
                JOptionPane.showMessageDialog(null, "Selecione uma embalagem para excluir");
            }
        }
    }//GEN-LAST:event_btnExcluirEmbalagemActionPerformed

